import numpy as np
# Generate arrays of zeros or ones

print(np.zeros(3)) #1D array of zeros
print('------------------------------------------------------')
print(np.zeros((4,5))) #4x5 zeros
print('------------------------------------------------------')
print(np.ones(3)) #1D array of ones
print('------------------------------------------------------')
print(np.ones((3,3))) #3x3 ones
print('------------------------------------------------------')
print(np.ones((5,1))) #5x1 ones