import pandas as pd
# Load the 'ds_salaries.csv' file into a DataFrame named 'df'
df=pd.read_csv('ds_salaries.csv')
print(df)
# [607 rows x 12 columns]
print('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
# generate a new column
df['bounce']=df['salary_in_usd']-700
print(df)
# [607 rows x 13 columns]
print('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
""" Useful Functions """
# head() ==> first n rows ,inside we can set how many rows we need to display ,by default 5
print(df.head())
print('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
# tail() ==> last n rows , inside we can set how many rows we need to display , by default 5
print(df.tail())
print('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
print(df.head(10))
print('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
print(df.head(100))
# [100 rows x 13 columns]
print('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
# check dtype of columns
print(df.dtypes)
('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
# Display a summary of the DataFrame
print(df.info())
('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
# shape of the DataFrame (rows, columns)
print(df.shape)
# (607, 13)
('------------------------------------------------------------------------------------------------------------------------------------------------------------------------------')
# Set the 'experience_level' column as the new index for the DataFrame and store it in 'indexed_data'
